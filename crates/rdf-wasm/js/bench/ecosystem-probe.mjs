const args = parseArgs(process.argv.slice(2));
const root = await mkdtemp(join(tmpdir(), "purrdf-ecosystem-"));
try {
  const packOutput = run("npm", ["pack", "--json", "--pack-destination", root], {
    cwd: PACKAGE_ROOT,
  });
  const purrdfPackument = parsePackument(packOutput, "npm pack for PurRDF");
  const packuments = { purrdf: purrdfPackument };
  for (const [name, version] of PACKAGE_SPECS) {
    const packument = parsePackument(
      run("npm", ["pack", "--json", "--pack-destination", root, `${name}@${version}`]),
      `npm pack for ${name}@${version}`,
    );
    packuments[name] = packument;
  }

  const project = join(root, "project");
  await mkdir(project);
  await writeFile(
    join(project, "package.json"),
    JSON.stringify({ private: true, type: "module" }, null, 2),
  );
  const installSpecs = [
    join(root, purrdfPackument.filename),
    ...PACKAGE_SPECS.map(([name, version]) => `${name}@${version}`),
  ];
  run("npm", ["install", "--ignore-scripts", "--no-audit", "--no-fund", ...installSpecs], {
    cwd: project,
    stdio: "inherit",
    timeout: NPM_INSTALL_TIMEOUT_MS,
  });

  const runnerPath = join(project, "probe-runner.mjs");
  await writeFile(runnerPath, runner);
  const raw = run(process.execPath, [runnerPath], {
    cwd: project,
    timeout: PROBE_TIMEOUT_MS,
  });
  const probe = JSON.parse(raw);
  const nodeModules = join(project, "node_modules");
  const packages = {
    purrdf: {
      key: "purrdf",
      ...(await packageInfo(
        nodeModules,
        "@blackcatinformatics/purrdf",
        purrdfPackument,
      )),
    },
  };
  for (const [name] of PACKAGE_SPECS) {
    packages[name] = {
      key: name,
      ...(await packageInfo(nodeModules, name, packuments[name])),
    };
  }

  const data = {
    generatedAt: new Date().toISOString(),
    runtime: {
      node: process.version,
      platform: process.platform,
      arch: process.arch,
    },
    commands: [
      "npm pack --json --pack-destination <tmp>",
      `npm install --ignore-scripts --no-audit --no-fund <local-purrdf.tgz> ${PACKAGE_SPECS.map(([name, version]) => `${name}@${version}`).join(" ")}`,
      "node <tmp>/project/probe-runner.mjs",
    ],
    corpus: probe.corpus,
    packages,
    results: probe.results,
  };

  const report = renderReport(data);
  if (args.report) {
    const reportPath = resolve(REPO_ROOT, args.report);
    await writeFile(reportPath, report);
    console.log(`wrote ${reportPath}`);
  } else {
    console.log(report);
  }
  if (args.json) {
    const jsonPath = resolve(REPO_ROOT, args.json);
    await writeFile(jsonPath, `${JSON.stringify(data, null, 2)}\n`);
    console.log(`wrote ${jsonPath}`);
  }
} finally {
  await rm(root, { force: true, recursive: true });
}
