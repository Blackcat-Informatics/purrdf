const SELECT_SIMPLE = "PREFIX ex: <https://bench.example/> SELECT ?o WHERE { ex:s42 ex:p ?o }";
const SELECT_PATH = "PREFIX ex: <https://bench.example/> SELECT ?name WHERE { ex:s0 ex:link+/ex:name ?name }";
const SELECT_AGG = "PREFIX ex: <https://bench.example/> SELECT (COUNT(?s) AS ?count) WHERE { ?s ex:p ?o }";

function unsupported(reason) {
  return { status: "unsupported", reason };
}

function fail(error) {
  return { status: "error", error: String(error?.message ?? error).split("\n")[0] };
}

async function measure(fn, detailFn) {
  const start = performance.now();
  let value;
  try {
    value = await fn();
  } catch (error) {
    return fail(error);
  }
  const ms = Number((performance.now() - start).toFixed(3));
  let detail;
  try {
    detail = detailFn ? detailFn(value) : undefined;
  } catch {
    detail = undefined;
  }
  return detail ? { status: "ok", ms, detail } : { status: "ok", ms };
}

async function graphyParse(graphy, input, format) {
  const reader = graphy.content[format].read();
  const quads = [];
  await new Promise((resolve, reject) => {
    reader.on("data", (quad) => quads.push(quad));
    reader.on("error", reject);
    reader.on("end", resolve);
    reader.end(input);
  });
  return quads;
}

async function graphyWrite(graphy, quads, format) {
  const writer = graphy.content[format].write();
  let output = "";
  await new Promise((resolve, reject) => {
    writer.on("data", (chunk) => {
      output += chunk;
    });
    writer.on("error", reject);
    writer.on("end", resolve);
    for (const quad of quads) writer.write(quad);
    writer.end();
  });
  return output;
}

